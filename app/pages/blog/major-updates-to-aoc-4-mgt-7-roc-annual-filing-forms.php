<h2 class="blog-caft-head">Introduction</h2>
<p class="mb-10">The Ministry of Corporate Affairs (MCA) has officially transitioned the final set of 38 forms from Version 2 to Version 3 of the MCA portal, effective from July 14, 2025. Among these, two key annual compliance forms—AOC-4 and MGT-7/MGT-7A—have undergone significant changes. This update is particularly relevant for companies filing 
for the financial year 2024–25. Below is a detailed summary of the key modifications in these forms and their implications for stakeholders.</p>
<h2 class="blog-caft-head">Key Changes in Filing of AOC-4</h2>

<p class="mb-10"><b>1. Annexures Now Integrated as Linked Forms</b></p>
<p class="mb-10">Documents such as:</p>
<ul class="bloglist">
    <li>Directors' Report</li>
<li>Auditors' Report</li>
<li>AOC-1 (Details of subsidiaries, associates, and joint ventures)</li>
<li>AOC-2 (Related party disclosures)</li>
</ul>
<p class="mb-10">are no longer required to be uploaded as separate PDFs. These are now integrated as linked forms within MCA V3. Post submission, these documents can be accessed as part of the filing workflow.</p>
<p class="mb-10"><b>2. CSR-2 & AOC-4 CFS Become Mandatory Linked Forms</b></p>
<p class="mb-10">Filing of CSR-2 and AOC-4 CFS is now integrated with AOC-4. These cannot be submitted independently anymore. Companies must complete these linked filings to ensure comprehensive disclosure of CSR activities and consolidated financials.</p>
<p class="mb-10"><b>3. Auto-Population of Previous Year Figures</b></p>
<p class="mb-10">The new form auto-fills financial data from the previous year with editable fields. However, any changes made require a compulsory justification to be entered in the form.</p>
<p class="mb-10"><b>4. Dual Filing Modes: Online & Offline</b></p>
<p class="mb-10">Filers now have the flexibility to submit AOC-4 through online or offline modes. The offline process involves entering basic company details online, downloading the Excel template, filling it offline, and re-uploading it.</p>
<p class="mb-10"><b>5. Secretarial Audit Qualifications to be Disclosed</b></p>
<p class="mb-10">A mandatory section has been introduced to disclose any qualifications reported by the Secretarial Auditor, improving governance transparency.</p>
<p class="mb-10"><b>6. AOC-4 Enabled for CIRP / Liquidation Entities</b></p>
<p class="mb-10">Companies undergoing Corporate Insolvency Resolution Process (CIRP) or liquidation can now file AOC-4 on the MCA portal, a feature previously unavailable.</p>
<p class="mb-10"><b>7. XBRL Taxonomies Remain Unchanged</b></p>
<p class="mb-10">The form will continue to support existing XBRL taxonomies for structured financial reporting, ensuring consistency with past filings.</p>


 <h3 class="blog-caft-head">Key Changes in Filing of MGT-7 / MGT-7A</h3>
<p class="mb-10"><b>1. Excel-Based Shareholder & Debenture Holder List</b></p>
<p class="mb-10">Details of shareholders and debenture holders are now to be uploaded using a standard Excel template. The file size limit for MGT-7 attachments has been increased to 300 MB (up to 15 files of 20 MB each). This change does not apply to MGT-7A.</p>
<p class="mb-10">The downloadable annual return will not display shareholder details, balancing privacy and statutory compliance.</p>
<p class="mb-10"><b>2. Gender-Wise Shareholding Data Introduced</b></p>
<p class="mb-10">A new section now captures gender-wise data of shareholders, allowing for enhanced demographic analysis of shareholding patterns.</p>
<p class="mb-10"><b>3. Registered Office Photograph Now Mandatory</b></p>
<p class="mb-10">Both interior and exterior photos of the registered office (clearly displaying the company nameboard) must be attached.</p>
<p class="mb-10"><b>4. Excel Template for Meeting Details</b></p>
<p class="mb-10">Board, Members, and Committee meeting details are now to be filled through a downloadable Excel format, streamlining structured disclosures.</p>
<p class="mb-10"><b>5. Year-End Name & Address Snapshot</b></p>
<p class="mb-10">The company's name and registered address as of the financial year-end will now be captured in a separate field. This ensures consistency where changes may occur between the year-end and filing date.</p>
<p class="mb-10"><b>6. Beneficial Ownership & Designated Person Details</b></p>
<p class="mb-10">Companies must now provide the details of individuals linked as beneficial owners and the designated person responsible for liaison with regulatory authorities.</p>
<p class="mb-10"><b>7. MGT-8 Integrated as Linked Form</b></p>
<p class="mb-10">MGT-8 is no longer required as a separate PDF upload. It will now be generated and submitted as a linked form, easing documentation effort.</p>
<p class="mb-10"><b>8. MGT-7 Enabled for CIRP / Liquidation Entities</b></p>
<p class="mb-10">Similar to AOC-4, MGT-7 can now be filed by companies under CIRP or liquidation, ensuring they meet compliance timelines.</p>
<p class="mb-10"><b>9. Dual Filing Modes Available</b></p>
<p class="mb-10">MGT-7 can also be filed through either online or offline modes, with data entry via Excel upload after basic online setup.</p>
<p class="mb-10">Redundant fields from the previous version of MGT-7 have been removed to simplify the overall filing process.</p>

 <h3 class="blog-caft-head">Conclusion</h3>
<p class="mb-10">The shift to MCA Version 3 represents a significant step toward digital transformation and improved regulatory compliance. The integration of linked forms, offline options, and standardized templates not only streamlines the process but also enforces consistency and transparency in statutory filings.</p>
<p class="mb-10">Staying ahead of these changes is crucial for ensuring timely and accurate compliance. If your business requires assistance in understanding or managing these new requirements, our team of professionals is here to help you navigate the transition with confidence.</p>


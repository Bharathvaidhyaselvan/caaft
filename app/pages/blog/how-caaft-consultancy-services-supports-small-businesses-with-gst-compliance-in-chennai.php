                    '&email=' + encodeURIComponent(email) +
                    '&phone=' + encodeURIComponent(phone) +
                    '&about=' + encodeURIComponent(selectedAbout) +
                    '&msg=' + encodeURIComponent(msg) +
                    '&title=' + encodeURIComponent(title);

